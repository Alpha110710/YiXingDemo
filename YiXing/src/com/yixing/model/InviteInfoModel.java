package com.yixing.model;

import com.google.gson.annotations.SerializedName;

public class InviteInfoModel extends BaseModel {
	
	@SerializedName("INVITE_CNT")
	private String friendCount;
	
	@SerializedName("AWARD_AMOUNT_TOTAL")
	private String receivedAmount;
	
	@SerializedName("INVITE_FRIEND_URL")
	private String inviteFriendUrl;
	
	public String getFriendCount() {
		return friendCount;
	}

	public void setFriendCount(String friendCount) {
		this.friendCount = friendCount;
	}

	
	public String getReceivedAmount() {
		return receivedAmount;
	}

	public void setReceivedAmount(String receivedAmount) {
		this.receivedAmount = receivedAmount;
	}

	public String getInviteFriendUrl() {
		return inviteFriendUrl;
	}

	public void setInviteFriendUrl(String inviteFriendUrl) {
		this.inviteFriendUrl = inviteFriendUrl;
	}

	
	
}