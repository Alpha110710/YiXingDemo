package com.yixing.model;

import com.google.gson.annotations.SerializedName;

public class ZQItemModel extends BaseModel{
	@SerializedName("BUY_DATE")
	private String time;//时间
	@SerializedName("BUY_AMOUNT")
	private String buyMoney;//购买金额
	@SerializedName("REAL_AMOUNT")
	private String money;//实付金额
	
	@SerializedName("RECOVER_DATE_FORMAT")
	private String RECOVER_DATE_FORMAT;// 预计还款时间
	@SerializedName("RECOVER_PERIOD")
	private String RECOVER_PERIOD;// 回款期数
	@SerializedName("RECOVER_AMOUNT_FORMAT")
	private String RECOVER_AMOUNT_FORMAT;// 回款金额
	
	
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getBuyMoney() {
		return buyMoney;
	}
	public void setBuyMoney(String buyMoney) {
		this.buyMoney = buyMoney;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	
	public String getRECOVER_DATE_FORMAT() {
		return RECOVER_DATE_FORMAT;
	}
	public void setRECOVER_DATE_FORMAT(String rECOVER_DATE_FORMAT) {
		RECOVER_DATE_FORMAT = rECOVER_DATE_FORMAT;
	}
	public String getRECOVER_PERIOD() {
		return RECOVER_PERIOD;
	}
	public void setRECOVER_PERIOD(String rECOVER_PERIOD) {
		RECOVER_PERIOD = rECOVER_PERIOD;
	}
	public String getRECOVER_AMOUNT_FORMAT() {
		return RECOVER_AMOUNT_FORMAT;
	}
	public void setRECOVER_AMOUNT_FORMAT(String rECOVER_AMOUNT_FORMAT) {
		RECOVER_AMOUNT_FORMAT = rECOVER_AMOUNT_FORMAT;
	}
	
	
}
