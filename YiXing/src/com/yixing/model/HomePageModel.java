package com.yixing.model;

import java.util.List;

import com.google.gson.annotations.SerializedName;

public class HomePageModel extends BaseModel {

	/**
	 * banner广告
	 */
	@SerializedName("ADVERTISING_LIST")
	private List<BannerItemModel> ADVERTISING_LIST;

	/**
	 * 新闻公告
	 */
	@SerializedName("NEW_ADVERTISING_LIST")
	private List<NewsAdvertisingModel> NEWS_INFO; // 替换类型

	/**
	 * 新标推荐
	 */

	@SerializedName("BULLETED_LIST")
	private List<HomeRecommendModel> BULLETED_LIST; // 替换类型

	public List<BannerItemModel> getADVERTISING_LIST() {
		return ADVERTISING_LIST;
	}

	public void setADVERTISING_LIST(List<BannerItemModel> aDVERTISING_LIST) {
		ADVERTISING_LIST = aDVERTISING_LIST;
	}

	public List<NewsAdvertisingModel> getNEWS_INFO() {
		return NEWS_INFO;
	}

	public void setNEWS_INFO(List<NewsAdvertisingModel> nEWS_INFO) {
		NEWS_INFO = nEWS_INFO;
	}

	public List<HomeRecommendModel> getBULLETED_LIST() {
		return BULLETED_LIST;
	}

	public void setBULLETED_LIST(List<HomeRecommendModel> bULLETED_LIST) {
		BULLETED_LIST = bULLETED_LIST;
	}

}
