package com.yixing.model;

import com.google.gson.annotations.SerializedName;

public class InvestmentDetailModel extends BaseModel {
	/**
	 * 产品状态（是否可以投资）
	 */
	@SerializedName("TENDER_FLG")
	private String flag;
	
	
	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	/**
	 * 产品类型（图标）
	 */
	@SerializedName("FINANCE_PRODUCTS_NAME")
	private String type;
	/**
	 * title
	 */
	@SerializedName("PRODUCTS_TITLE")
	private String title;

	/**
	 * 年华收益
	 */
	@SerializedName("FINANCE_INTEREST_RATE")
	private String earning_Rate;
	/**
	 * 产品期限
	 */
	@SerializedName("INTEREST_RATE_TYPE")
	private String product_Deadline;
	/**
	 * 产品期限单位
	 */
	@SerializedName("PRODUCT_DEALINE_TYPE")
	private String product_Deadline_Type;
	/**
	 * 融资金额
	 */
	@SerializedName("FINANCE_AMOUNT")
	private String product_Money;
	/**
	 * 融资金额单位
	 */
	@SerializedName("PRODUCT_MONEY_TYPE")
	private String product_Money_type;
	/**
	 * 剩余可投金额
	 */
	@SerializedName("FINANCE_AMOUNT_WAIT")
	private String product_Remain_Money;
	/**
	 * 剩余可投金额单位
	 */
	@SerializedName("PRODUCT_REMAIN_MONEY_TYPE")
	private String product_Remain_Money_Type;
	/**
	 * 协议范本
	 */
	@SerializedName("PRODUCT_TEXT")
	private String product_text;
	/**
	 * 还款方式
	 */
	@SerializedName("FINANCE_REPAY_TYPE")
	private String modeRepayment;
	/**
	 * 项目编号
	 */
	@SerializedName("PRODUCTS_CODE")
	private String productsCode;

	public String getProduct_text() {
		return product_text;
	}

	public void setProduct_text(String product_text) {
		this.product_text = product_text;
	}

	public String getProduct_Remain_Money_Type() {
		return product_Remain_Money_Type;
	}

	public void setProduct_Remain_Money_Type(String product_Remain_Money_Type) {
		this.product_Remain_Money_Type = product_Remain_Money_Type;
	}


	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}


	public String getEarning_Rate() {
		return earning_Rate;
	}

	public void setEarning_Rate(String earning_Rate) {
		this.earning_Rate = earning_Rate;
	}

	public String getProduct_Deadline() {
		return product_Deadline;
	}

	public void setProduct_Deadline(String product_Deadline) {
		this.product_Deadline = product_Deadline;
	}

	public String getProduct_Deadline_Type() {
		return product_Deadline_Type;
	}

	public void setProduct_Deadline_Type(String product_Deadline_Type) {
		this.product_Deadline_Type = product_Deadline_Type;
	}

	public String getProduct_Money() {
		return product_Money;
	}

	public void setProduct_Money(String product_Money) {
		this.product_Money = product_Money;
	}

	public String getProduct_Money_type() {
		return product_Money_type;
	}

	public void setProduct_Money_type(String product_Money_type) {
		this.product_Money_type = product_Money_type;
	}

	public String getProduct_Remain_Money() {
		return product_Remain_Money;
	}

	public void setProduct_Remain_Money(String product_Remain_Money) {
		this.product_Remain_Money = product_Remain_Money;
	}

	public String getModeRepayment() {
		return modeRepayment;
	}

	public void setModeRepayment(String modeRepayment) {
		this.modeRepayment = modeRepayment;
	}
	public String getProductsCode() {
		return productsCode;
	}
	
	public void setProductsCode(String productsCode) {
		this.productsCode = productsCode;
	}

}
